def ZUC_DDT():
    S0_DDT, S1_DDT = list(), list()
    for diff in range(0x000, 0x100):
        S0_count, S1_count = dict(), dict()
        S0_line, S1_line = list(), list()
        for x in range(0x000, 0x100):
            x1 = x ^ diff
            S0_output, S1_output = S0[x] ^ S0[x1], S1[x] ^ S1[x1]
            S0_count[S0_output] = S0_count.get(S0_output, 0) + 1
            S1_count[S1_output] = S1_count.get(S1_output, 0) + 1
        for i in range(0x000, 0x100):
            S0_line.append(S0_count.get(i, 0))
            S1_line.append(S1_count.get(i, 0))
        S0_DDT.append(S0_line)
        S1_DDT.append(S1_line)
    return S0_DDT, S1_DDT


def ZUC_LAT():
    S0_LAT, S1_LAT = list(), list()
    for i in range(0x000, 0x100):
        S0_line, S1_line = list(), list()
        for j in range(0x000, 0x100):
            S0_count, S1_count = 0, 0
            for x in range(0x000, 0x100):
                if i & x == j & S0[x]:
                    S0_count += 1
                if i & x == j & S1[x]:
                    S1_count += 1
            S0_line.append(S0_count-0x80)
            S1_line.append(S1_count-0x80)
        S0_LAT.append(S0_line)
        S1_LAT.append(S1_line)
    return S0_LAT, S1_LAT


